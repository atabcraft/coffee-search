package com.example.search.coffee.domain;

/**
 * Enumerable defining types of coffee.
 * @author Armin
 */
public enum CoffeeType {
    /***
     * Robusta coffee is coffee made from the Coffea canephora plant, a sturdy species of coffee bean with low acidity and high bitterness
     */
    ROBUSTA,
    /**
     * Coffea arabica (/əˈræbɪkə/), also known as the Arabian coffee, "coffee shrub of Arabia", "mountain coffee", or "arabica coffee", is a species of Coffea.
     */
    ARABICA
    
}
